def cmd_query(config: DBConfig, sql: str):
    """Run a SQL query."""
    conn = get_connection(config)
    if not conn:
        print_fail("Cannot connect to database")
        return

    cur = conn.cursor()
    try:
        cur.execute(sql)
        if cur.description:
            rows = cur.fetchall()
            cols = [d[0] for d in cur.description]
            print(f"\n{' Col ':─<30} " * len(cols))
            header = "  ".join(f"{c:<30}" for c in cols)
            print(header)
            print("─" * 80)
            for row in rows[:20]:
                print("  ".join(f"{str(v)[:30]:<30}" for v in row))
            if len(rows) > 20:
                print(f"\n  ... {len(rows) - 20} more rows")
            print(f"\n  {len(rows)} rows returned")
        else:
            print_ok(f"Query executed, {cur.rowcount} rows affected")
    except Exception as e:
        print_fail(f"Query failed: {e}")
    finally:
        cur.close()
        conn.close()



def cmd_docker_compose():
    """Generate docker-compose for PostgreSQL."""
    import uuid

    password = str(uuid.uuid4())[:16]
    print_info("Generating docker-compose.yml for PostgreSQL...")

    content = DOCKER_COMPOSE_PG.format(password=password)
    path = PROJECT_ROOT / "docker-compose.pg.yml"
    with open(path, "w") as f:
        f.write(content)

    print_ok(f"Created {path}")
    print_info(f"Generated password: {password}")
    print_info("Save this password! Add to your .env:")
    print(f"  DB_PASSWORD={password}")



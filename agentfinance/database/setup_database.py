def main():
    parser = argparse.ArgumentParser(description="AgentFinance PostgreSQL Setup")
    parser.add_argument("--status", action="store_true", help="Check connection status")
    parser.add_argument("--init", action="store_true", help="Initialize schema")
    parser.add_argument("--seed", action="store_true", help="Add seed data")
    parser.add_argument(
        "--drop", action="store_true", help="Drop existing tables first"
    )
    parser.add_argument(
        "--docker-compose",
        action="store_true",
        help="Generate Docker Compose for PostgreSQL",
    )
    parser.add_argument("--query", type=str, help="Run SQL query")
    parser.add_argument(
        "--env", choices=["docker", "local"], default="local", help="Connection mode"
    )
    args = parser.parse_args()

    config = DBConfig.from_env() if args.env == "local" else DBConfig.from_docker()

    if args.docker_compose:
        cmd_docker_compose()
        return

    if args.status:
        cmd_status(config)
        return

    if args.init:
        success = cmd_init(config, drop_existing=args.drop)
        sys.exit(0 if success else 1)
        return

    if args.seed:
        cmd_seed(config)
        return

    if args.query:
        cmd_query(config, args.query)
        return

    # Default: interactive
    print(f"\n{'═' * 50}")
    print(f"  AgentFinance Database Setup")
    print(f"{'═' * 50}\n")
    print("Usage:")
    print("  python3 setup_database.py --status         # Check connection")
    print("  python3 setup_database.py --init           # Create schema")
    print("  python3 setup_database.py --init --drop   # Recreate schema")
    print("  python3 setup_database.py --seed           # Add sample data")
    print("  python3 setup_database.py --docker-compose # Generate Docker Compose")
    print()
    print(f"Connection: {config.host}:{config.port}/{config.database}")
    print()


if __name__ == "__main__":
    main()

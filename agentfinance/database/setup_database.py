def cmd_status(config: DBConfig):
    """Check database connection status."""
    print(f"\n{'═' * 50}")
    print(f"  PostgreSQL Connection Status")
    print(f"{'═' * 50}\n")

    conn = get_connection(config)
    if conn:
        cur = conn.cursor()
        cur.execute("SELECT version();")
        version = cur.fetchone()[0]
        cur.execute("SELECT pg_database_size(%s);", (config.database,))
        size = cur.fetchone()[0]
        cur.execute("SELECT COUNT(*) FROM pg_tables WHERE schemaname = 'public';")
        tables = cur.fetchone()[0]

        print_ok(f"Connected to: {config.host}:{config.port}/{config.database}")
        print(f"  PostgreSQL: {version.split(',')[0]}")
        print(f"  DB Size: {size / 1024 / 1024:.1f} MB")
        print(f"  Tables: {tables}")
        cur.close()
        conn.close()
    else:
        print_fail(f"Cannot connect to {config.host}:{config.port}")
        print_info("Options:")
        print(
            "  1. Start PostgreSQL: docker run -d -p 5432:5432 -e POSTGRES_PASSWORD=agentfinance postgres:16"
        )
        print("  2. Use Docker Compose (see docker-compose.yml)")
        print("  3. Install locally and run: sudo systemctl start postgresql")


def cmd_init(config: DBConfig, drop_existing: bool = False):
    """Initialize the database schema."""
    print(f"\n{'═' * 50}")
    print(f"  Initializing Database Schema")
    print(f"{'═' * 50}\n")

    conn = get_connection(config)
    if not conn:
        print_fail("Cannot connect to database")
        return False

    cur = conn.cursor()

    try:
        if drop_existing:
            print_warn("Dropping existing tables...")
            cur.execute("""
                DROP TABLE IF EXISTS equity_snapshots CASCADE;
                DROP TABLE IF EXISTS trades CASCADE;
                DROP TABLE IF EXISTS signals CASCADE;
                DROP TABLE IF EXISTS performance_metrics CASCADE;
                DROP TABLE IF EXISTS session_analysis CASCADE;
                DROP TABLE IF EXISTS ohlcv_data CASCADE;
                DROP TYPE IF EXISTS trade_direction CASCADE;
                DROP TYPE IF EXISTS order_status CASCADE;
                DROP TYPE IF EXISTS setup_type CASCADE;
                DROP TYPE IF EXISTS session_name CASCADE;
                DROP TYPE IF EXISTS timeframe_enum CASCADE;
            """)
            conn.commit()

        # Execute schema
        schema = SCHEMA_SQL.format(timestamp=datetime.now(timezone.utc).isoformat())
        cur.execute(schema)
        conn.commit()

        # Count created tables
        cur.execute("""
            SELECT COUNT(*) FROM pg_tables
            WHERE schemaname = 'public'
            AND tablename NOT LIKE 'pg_%'
            AND tablename NOT LIKE 'sql_%';
        """)
        tables = cur.fetchone()[0]

        print_ok(f"Schema initialized successfully")
        print(f"  Tables created: {tables}")
        print(
            f"  Views created: 3 (v_daily_performance, v_setup_performance, v_equity_curve)"
        )

    except Exception as e:
        print_fail(f"Schema initialization failed: {e}")
        conn.rollback()
        return False
    finally:
        cur.close()
        conn.close()

    return True


